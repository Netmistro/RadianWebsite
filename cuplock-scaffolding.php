    <h3>The CUPLOK Locking Procedure</h3>
    <p>
        At the heart of the CUPLOK system is its unique node-point locking device. This enables up to four horizontals
        to be loosely but safely connected to the standard then locked into position with a single hammer blow. The
        system uses no loose clips, bolts or wedges. The locking device is formed by fixed lower cups, welded to the
        standards at 0.5m intervals, and sliding upper cups which drop over the blade ends of the horizontals and rotate
        to lock them firmly into place giving a positive, rigid connection. This revolutionary design makes CUPLOK
        system quick and easy to erect and dismantle, saving the client in labour costs. Once a CUPLOK structure is
        ‘based out’ and levelled, subsequent lifts are automatically erected square and horizontal. The lack of loose
        components makes the system easy to use and exceptionally robust - its galvanised finish making it virtually
        immune to corrosion and damage. (SGB 2018)
