        <h3>Typical requirements for Engineered Design:-</h3>
        <ol>
            <li>Drawings - For fastest design turnover, AutoCAD Drawings are the best, as they would save alot of time;
                if not PDF Drawings would suffice. available Structural steel plan of the layout where the scaffold is
                to be constructed. This would include all elevations and plans associated with the area where the
                scaffold is to be installed. If there are no drawings available, a sketch would have to work. The sketch
                should include the general area, the location and size of the scaffold and its general positioning.</li>
            <li>In order to comply with Eurocode, the client need to provide the fundamental value of basic wind
                velocity as 10 minutes mean wind velocity irrespective of wind direction and time of year 10m above
                ground having probabilities of exceedance of 0.02 which is equivalent to mean return period of 50 years.
                If this is not a consideration, the design can still be done but will not be in compliance with
                Eurocode.</li>
            <li>Scaffold - The general size (L x B x H) and purpose of the scaffold.</li>
            <li>Ground Conditions - This is often overlooked and is something thats very crutial to designing any ground
                up scaffold. If you have BS 5975, as there is a lot guidance within. If the ground is soft, it maybe
                necessary to spread the load using additional sole boards. Know what you are up against and let the
                design team be aware.</li>
            <li>Photographs - Photos of the general area where the scaffold is to be erected. At least two photos should
                be taken. One of the general area showing any nearby cables or obstacles that can pose an issue during
                the scaffold erection and another showing the exact location where the proposed scaffold is to be
                erected. If more than two photos can be provided, that would be even better.</li>
            <li>End Users - Please consider the end users of your scaffolding. It is very important that scaffolds be
                built for purpose and the correct loading requirements are understood from inception. This could save
                a lot of rework, miscommunication and unnecessary expense.</li>
            <li>Potential Site Hazards - Take a smuch photos as you can about the area and what could possibly be a
                hinderance and a hazard to erecting the scaffold.</li>
        </ol>
