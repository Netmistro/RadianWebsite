        <h2>Getmie Safe Rescue System</h2>
        <p>
            The Fast Access lifting, and descending device is a rescue system by means of which one or two persons,
            or several persons one after the other are able to evacuate from a higher to a lower place at limited
            velocity. These devices are ideal for evacuation from cranes, hanging scaffolds and other structures.
            In addition, a person can be lifted or lowered from one level to another by a rescuer or the rescuer can
            descend to a casualty, collect that person and lower them to safety.
            The Fast Access model has two lifting ratchets to allow an increase in lifting speed.
            With the addition of the rescue pole a casualty can be connected to the system from up to 5.4 metres
            away and lifted or lowered to safety making this a ‘no risk’ situation for the rescuer. Various rope
            lengths of up to 160m can be provided to suit requirements. Standard length is 30m.
        </p><br>
        <p>
            These devices have many applications, these are as follows:
        <ol>
            <li>Evacuation of one or more persons from a high to lower area, i.e.; cranes or hanging scaffolding
            </li>
            <li>No risk rescue of a suspended person using either 3.5m or 6.5m long reach pole</li>
            <li>Lifting a casualty from a lower area or from a suspended position</li>
            <li>Lowering of a casualty or stretcher</li>
            <li>Descending to a casualty to retrieve them by either lifting or lowering</li>
            <li>Confined space rescue. Either with a conventional tripod or used within a vessel to lift the
                casualty over baffle plates or other obstructions
            </li>
        </ol>
        </p>
